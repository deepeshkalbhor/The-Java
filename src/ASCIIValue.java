public class ASCIIValue {
    public static void main(String[] args) {
        char ch1 = 'a';
        char ch2 = 'b';

        int asciiVal1 = ch1;
        int asciiVal2 = ch2;

        System.out.println("The ASCII value of " + ch1 + " is " + asciiVal1);
        System.out.println("The ASCII value of " + ch2 + " is " + asciiVal2);
    }
}
